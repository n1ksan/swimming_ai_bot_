def _days_since(date_str: str) -> int:
    try:
        last = datetime.strptime(date_str[:10], "%Y-%m-%d").date()
        return (datetime.now().date() - last).days
    except Exception:
        return 0


def _effort_trend(completed: list) -> str:
    efforts = [w["perceived_effort"] for w in completed[:3] if w["perceived_effort"]]
    if len(efforts) < 2:
        return ""
    if efforts[0] > efforts[-1]:
        return "нагрузка нарастает (последние оценки растут)"
    if efforts[0] < efforts[-1]:
        return "нагрузка снижается (последние оценки падают)"
    return "нагрузка стабильна"


def _repeated_complaints(completed: list) -> str:
    feedbacks = [w["feedback"] for w in completed[:5] if w["feedback"]]
    if not feedbacks:
        return ""
    return "; ".join(feedbacks)


def _build_history_context(history: list) -> str:
    completed = [w for w in history if w["completed"]]
    if not completed:
        return (
            "\n\nИСТОРИЯ: выполненных тренировок нет. "
            "Составь базовую тренировку под текущий уровень пловца."
        )

    lines = ["\n\nАНАЛИТИКА И ИСТОРИЯ:"]

    # Дней с последней тренировки
    days_off = _days_since(completed[0]["date"])
    if days_off > 7:
        lines.append(f"⚠️ Перерыв: {days_off} дней без тренировки — снизь объём на уровень двух тренировок назад.")
    elif days_off > 3:
        lines.append(f"Дней отдыха с последней тренировки: {days_off}.")
    else:
        lines.append(f"Дней отдыха с последней тренировки: {days_off}.")

    # Нужна ли восстановительная тренировка
    count = len(completed)
    if count % 4 == 0:
        lines.append(f"🔄 Каждые 4 тренировки — восстановительная. Сейчас тренировка #{count + 1}: дай лёгкую восстановительную (70% обычного объёма).")

    # Тренд нагрузки
    trend = _effort_trend(completed)
    if trend:
        lines.append(f"Тренд: {trend}.")

    # Повторяющиеся жалобы
    complaints = _repeated_complaints(completed)
    if complaints:
        lines.append(f"Комментарии пловца за последние тренировки: {complaints}")

    lines.append("\nВЫПОЛНЕННЫЕ ТРЕНИРОВКИ (от новейшей к старейшей):")
    for i, w in enumerate(completed, 1):
        parts = [f"#{i} | {w['date']}"]
        if w["distance_meters"]:
            parts.append(f"объём {w['distance_meters']} м")
        if w["perceived_effort"]:
            effort = w["perceived_effort"]
            label = "лёгкая" if effort <= 4 else ("оптимальная" if effort <= 7 else "тяжёлая")
            parts.append(f"нагрузка {effort}/10 ({label})")
        if w["feedback"]:
            parts.append(f"комментарий: «{w['feedback']}»")
        lines.append(" | ".join(parts))

    return "\n".join(lines)


